            notes section. (See next step)</b>
          </li>

          <li>
            In the notes section, record your corrections and number each correction starting
            with #1.
          </li>

          <li>
            State the original sentence with the mistake, and then how it should be corrected.
            <br><br>
          </li>

          <ul>
            <li>
              “Should read”: this is for most mistakes that require a simple correction, such as
              phrases that include simple mistakes.
              <br><br>
              <b>Example:</b>
              <br>
              "....on which you should exercise your discretion..." should read "...in which you
              should exercise your discretion...”
              <br><br>
            </li>

            <li>
              “Was deleted”: this is used to record words, punctuation or phrases that were
              accidentally added by the transcriber and deleted by the proofreader. Write
              “(Deleted)” in the location of the removed phrase. This allows you to write a
              comment where you state what you deleted.
              <br><br>
              <b>Example:</b>
              <br>
              “Words don’t come easy…” was deleted.
              <br><br>
            </li>

            <li>
              “Was added”: this is used to indicate missing content that the transcriber missed
              in the archival document. <b>If you see any missing text in the archival
